//Arithmatic Operators
let a = 5, b = 2;
console.log("a =", a,"& b =", b);
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);


//Unary Operator
a = a + 1;
console.log("a =", a);
a++;
console.log("a =", a);
a--;
